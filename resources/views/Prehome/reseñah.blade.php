                “Calipuerto” iniciara los trabajos de ampliación de sus instalaciones y pista. Estos trabajos hicieron
                posible que “Calipuerto” prolongara durante veintiocho años más su vida útil.</p>

            <p class="parrafo-l">En 1954 se propuso la construcción para Cali de un nuevo aeropuerto de magnitud
                internacional. La localización técnica inicio ese mismo año y, un año después, se efectuaron estudios
