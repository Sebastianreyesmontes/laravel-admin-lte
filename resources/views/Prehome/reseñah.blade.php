            <p class="parrafo-l">El 21 de septiembre de 1933 se dio inicio a vuelos comerciales y militares en la base de “El
                Guabito”. Ese día, aeronaves internacionales de Panagra y nacionales de Scadta unían a Cali con las
                principales ciudades colombianas y a Colombia con otros países.</p>

            <p class="parrafo-l">El uso alterno de la pista de “El Guabito” no duró mucho tiempo, debido a que los vuelos
                mixtos de aeronaves comerciales y aeronaves de aprendizaje no eran aconsejables dentro de una base militar.
                Años después se produciría la separación de esos vuelos al tiempo que Avianca, nombre sustituto de la
                pionera Scadta, adquiriera los terrenos de la antigua hacienda “Guales”, sobre la carretera Cali-Candelaria,
                situada a 18 kilómetros al oriente de la capital vallecaucana.</p>

            <p class="parrafo-l">En 1943 comienza a operar un nuevo aeropuerto, bautizado con el nombre de Calipuerto. Este
                aeropuerto daba a Colombia la imagen de un país “desordenado y folclórico”. La sala de recibo de los
                viajeros era estrecha, incomoda e inadecuada para el creciente movimiento de llegada y salida de gentes de
                distintas nacionalidades.</p>

            <p class="parrafo-l">Por la década de 1950 surgieron rumores encaminados hacia la necesidad que tenía Cali de
