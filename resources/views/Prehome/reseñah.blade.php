                tiempo se elevaron las propuestas para los terminales aéreos de las capitales del Valle y del Atlántico.
                Poco después la Junta Técnica del ministerio de Obras Publicas descartó las dos solicitudes.</p>

            <p class="parrafo-l">La Unidad de Acción Vallecaucana, a pesar del rechazo, continuó en la lucha con ejemplar
